// tugas membuat project juragan angkot di console menggunakan pengulangan for

var jmlAngkot = 10;
var angkotBeroprasi = 6;
var noAngkot = 1;

for (noAngkot = noAngkot; noAngkot <= angkotBeroprasi; noAngkot++ ) {
	console.log('angkot no. ' + noAngkot + ' beroprasi dengan baik')
} 

for (noAngkot = angkotBeroprasi + 1; noAngkot <= jmlAngkot; noAngkot++ ) {
	console.log('angkot no. ' + noAngkot + ' beroprasi dengan baik')
}